void FAN_Init(void){
	PWM_Init();
}
void FAN_SetDirection(FAN_dir_e direction){
	switch(direction){
		case FAN_DIRECTION_RIGHT : DIO_WriteChannel(FAN_PIN1,PIN_HIGH); 
									DIO_WriteChannel(FAN_PIN2,PIN_LOW);
									break;
		case FAN_DIRECTION_LEFT : DIO_WriteChannel(FAN_PIN1,PIN_LOW);
									 DIO_WriteChannel(FAN_PIN2,PIN_HIGH);
									 break;
		default: break;
	}
}
void FAN_SetSpeed(uint8 Speed){
	PWM2_Generate(Speed);
}
void FAN_Stop(void){
	DIO_WriteChannel(FAN_PIN1,PIN_HIGH);
	DIO_WriteChannel(FAN_PIN2,PIN_HIGH);
}
