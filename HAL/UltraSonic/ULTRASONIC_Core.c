void Ultrasonic_Init(void){
	ICU_Init();
}
void Ultrasonic_Trigger(void){
	SET_ICU_RISING_EDGE();
	DIO_WriteChannel(ULTRASONIC_TRIG_PIN , PIN_HIGH);
	_delay_us(10);
	DIO_WriteChannel(ULTRASONIC_TRIG_PIN , PIN_LOW);
}
uint32 Ultrasonic_GetDistance(void){
	uint32 Time =0;
	uint32 Distance =0; 
	Time =ICU_CaptureOnTime();
	Distance = (0.034 * Time)/2;
	return Distance;
}

